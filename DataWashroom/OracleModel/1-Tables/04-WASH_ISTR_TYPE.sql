CREATE TABLE VPI.WASH_ISTR_TYPE
(
	ISTR_TYPE		VARCHAR2(16)		NOT NULL,
	ISTR_BRIEF		VARCHAR2(32)		NOT NULL,
	DESCRIPTION_	VARCHAR2(128),

	CONSTRAINT PK_WASH_ISTR_TYPE PRIMARY KEY (ISTR_TYPE),
	CONSTRAINT CK_WASH_ISTR_TYPE_T CHECK (ISTR_TYPE = UPPER(TRIM(ISTR_TYPE)))
)	STORAGE (INITIAL 16K NEXT 8K);
